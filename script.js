function openNav() {
  document.getElementById("nav").style.width = "300px";
  document.getElementById("main").style.marginLeft = "300px";
}

function closeNav() {
  document.getElementById("nav").style.width = "0";
  document.getElementById("main").style.marginLeft= "0";
}
